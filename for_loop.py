# Exercise 5: Square Numbers
# initialize a variable to store the sum
sum_of_squares = 0    

print("Exercise 5: Square numbers")
for number in range(1,6):
    square = number ** 2
    print(square)

    sum_of_squares += square

print("Sum of the squares is: " ,sum_of_squares)



